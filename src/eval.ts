import { writeFileSync } from "node:fs";
import type { Difficulty } from "./types.js";
import { generationBudget, resolveBoard } from "./boards.js";
import { generate } from "./generate.js";
import { logicSolve } from "./logic.js";
import { makeRng } from "./rng.js";
import { reduceUnique } from "./reduce.js";
import { randomSolution } from "./solver.js";
import { stateToAssignment } from "./state.js";

/**
 * Difficulty study for the star boards. For each board ×
 * difficulty × seed it runs the full shipped generation pipeline (same budgets
 * as the web worker) and records the band hit rate, clue density, per-tier
 * solver work, and wall time. A second pass measures, per board, how often a
 * *uniqueness-minimal* puzzle (unique solution, but not curated for logic)
 * is solvable without guessing — a proxy for how much of the puzzle space is
 * human-playable at all.
 *
 *   npm run eval          # writes star-eval.md and prints it
 */

const BOARDS = ["s7", "s9", "s11", "s15"];
const DIFFS: Difficulty[] = ["normal", "hard", "really"];

/** Seeds per (board, difficulty): fewer on big boards to keep runtime sane. */
function seedCount(cells: number): number {
  if (cells <= 100) return 15;
  if (cells <= 150) return 10;
  return 6;
}

interface GenRow {
  board: string;
  cells: number;
  difficulty: Difficulty;
  n: number;
  hits: number;
  ratings: Record<string, number>;
  clues: number[];
  t1: number[];
  t2: number[];
  t3: number[];
  t4: number[];
  ms: number[];
}

const avg = (xs: number[]) => xs.reduce((a, b) => a + b, 0) / xs.length;
const pct = (x: number) => `${Math.round(x * 100)}%`;
const f1 = (x: number) => x.toFixed(1);

function runGeneration(): GenRow[] {
  const rows: GenRow[] = [];
  for (const token of BOARDS) {
    const board = resolveBoard(token);
    const cells = board.order.length;
    const n = seedCount(cells);
    for (const difficulty of DIFFS) {
      const budget = generationBudget(cells, difficulty);
      const row: GenRow = {
        board: token,
        cells,
        difficulty,
        n,
        hits: 0,
        ratings: {},
        clues: [],
        t1: [],
        t2: [],
        t3: [],
        t4: [],
        ms: [],
      };
      for (let seed = 1; seed <= n; seed++) {
        const start = Date.now();
        const puzzle = generate(board, difficulty, makeRng(seed), budget);
        row.ms.push(Date.now() - start);
        if (puzzle.rating === difficulty) row.hits++;
        row.ratings[puzzle.rating] = (row.ratings[puzzle.rating] ?? 0) + 1;
        row.clues.push(puzzle.clues.size);
        row.t1.push(puzzle.counts.tier1);
        row.t2.push(puzzle.counts.tier2);
        row.t3.push(puzzle.counts.tier3);
        row.t4.push(puzzle.counts.tier4);
      }
      rows.push(row);
      console.error(
        `  ${token} ${difficulty}: ${row.hits}/${n} on band, ` +
          `~${Math.round(avg(row.clues))} clues, ~${Math.round(avg(row.ms))}ms`,
      );
    }
  }
  return rows;
}

interface StuckRow {
  board: string;
  n: number;
  stuck: number;
  clues: number[];
}

/** Uniqueness-minimal reduction is countSolutions-heavy; keep to small boards. */
function runStuckStudy(): StuckRow[] {
  const rows: StuckRow[] = [];
  for (const token of BOARDS) {
    const board = resolveBoard(token);
    if (board.order.length > 100) continue;
    const row: StuckRow = { board: token, n: 6, stuck: 0, clues: [] };
    for (let seed = 1; seed <= row.n; seed++) {
      const solution = randomSolution(board, makeRng(seed));
      const clues = reduceUnique(solution, board, makeRng(seed + 1000), 2);
      row.clues.push(clues.size);
      if (logicSolve(clues, board, 4).status !== "solved") row.stuck++;
    }
    rows.push(row);
    console.error(`  ${token}: ${row.stuck}/${row.n} uniqueness-minimal puzzles need guessing`);
  }
  return rows;
}

function main(): void {
  console.error("Generation study (shipped budgets)...");
  const gen = runGeneration();
  console.error("Uniqueness-minimal guessing study...");
  const stuck = runStuckStudy();

  const lines: string[] = [];
  lines.push("# Star variant difficulty evaluation");
  lines.push("");
  lines.push(
    "Auto-generated by `npm run eval`; re-running overwrites this file " +
      "(including any hand-written findings).",
  );
  lines.push(
    "Boards `sN` are odd squares (each line: (n−1)/2 white, (n−1)/2 black, 1 star).",
  );
  lines.push("");
  lines.push("## Generation study");
  lines.push("");
  lines.push(
    "Full shipped pipeline (same budgets as the web worker), N seeds per cell.",
  );
  lines.push(
    "**band hit** = generated puzzle rates exactly at the requested difficulty;",
  );
  lines.push(
    "**tiers** = average solver invocations per tactic tier on the shipped puzzle.",
  );
  lines.push("");
  lines.push(
    "| board | cells | difficulty | N | band hit | ratings seen | avg clues | clue % | tiers t1/t2/t3/t4 | avg gen ms |",
  );
  lines.push("|---|---|---|---|---|---|---|---|---|---|");
  for (const r of gen) {
    const ratings = Object.entries(r.ratings)
      .map(([k, v]) => `${k}:${v}`)
      .join(" ");
    lines.push(
      `| ${r.board} | ${r.cells} | ${r.difficulty} | ${r.n} | ${pct(r.hits / r.n)} | ${ratings} | ` +
        `${f1(avg(r.clues))} | ${pct(avg(r.clues) / r.cells)} | ` +
        `${f1(avg(r.t1))}/${f1(avg(r.t2))}/${f1(avg(r.t3))}/${f1(avg(r.t4))} | ${Math.round(avg(r.ms))} |`,
    );
  }
  lines.push("");
  lines.push("## How much of the puzzle space is playable without guessing?");
  lines.push("");
  lines.push(
    "Uniqueness-minimal reduction (unique solution, *not* curated for logic), then",
  );
  lines.push(
    "the graded no-guessing solver. \"Stuck\" puzzles are unfair as deduction",
  );
  lines.push("puzzles. Small boards only (the check is search-heavy).");
  lines.push("");
  lines.push("| board | N | needs guessing | avg clues (minimal) |");
  lines.push("|---|---|---|---|");
  for (const r of stuck) {
    lines.push(
      `| ${r.board} | ${r.n} | ${r.stuck}/${r.n} | ${f1(avg(r.clues))} |`,
    );
  }
  lines.push("");

  const md = lines.join("\n");
  writeFileSync("star-eval.md", md + "\n");
  console.log(md);
  console.error("\nWrote star-eval.md");
}

main();
